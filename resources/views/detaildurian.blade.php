                            <p style="font-size: 20px;">Durian (Durio zibethinus) adalah buah tropis yang terkenal dengan aroma khasnya yang kuat dan rasa manis yang unik. Buah ini sering disebut sebagai "raja dari segala buah" karena cita rasanya yang begitu khas dan disukai oleh banyak orang.</p>
                            <h3>Karakteristik</h3>
                            <ul style="font-size: 20px;">
                                <li>Kulit: Durian memiliki kulit luar yang keras dan berduri tajam.</li>
                                <li>Daging: Daging buah durian berwarna kuning cerah hingga oranye, bertekstur lembut dan creamy.</li>
                                <li>Aroma: Aroma durian sangat khas dan kuat, seringkali dianggap menyengat oleh sebagian orang, namun bagi pencintanya, aroma ini sangat menggoda.</li>
                                <li>Biji: Di dalam daging buah durian terdapat biji yang berwarna cokelat dan berukuran cukup besar.</li>
                                <li>Warna : Warna kulitnya bervariasi, mulai dari hijau hingga cokelat keemasan, tergantung varietasnya.</li>
                            </ul>
                            <h3>Manfaat</h3>
                            <ul style="font-size: 20px;">
                                <li>Meningkatkan energi</li>
                                <li>Meningkatkan sistem kekebalan imun tubuh</li>
                                <li>Menjaga kesehatan pencernaan</li>
                                <li>Menjaga kesehatan kulit</li>
                            </ul>
                            <h3>Varietas Durian</h3>
                            <ul>
                                <li>Durian Montong</li>
                                <li>Durian Musang King</li>
                                <li>Durian Kanayo</li>
                                <li>Durian Tekong</li>
                            </ul>
                            <h3>Merawat Tanaman Buah Durian</h3>
                            <p style="font-size: 20px;">Merawat pohon durian agar menghasilkan buah yang berkualitas. Perhatikan pemilihan bibit unggul, siapkan tanah yang subur dan gembur, serta lakukan pemupukan secara teratur. Jaga kelembaban tanah dengan penyiraman yang cukup, terutama saat musim kemarau. Lakukan pemangkasan rutin untuk merangsang pertumbuhan cabang dan bunga. Lindungi pohon dari hama dan penyakit dengan pestisida organik. Dengan perawatan yang tepat, pohon durian akan tumbuh subur dan menghasilkan buah yang lezat.</p>
                            <h3>Media Tanam</h3>
                            <p style="font-size: 20px;">Sebelum menanam durian, sebaiknya persiapkan media tanam dengan baik. Campurkan tanah asli dengan pupuk kandang matang, kompos, dan pasir. Perbandingan campuran ini bisa disesuaikan dengan kondisi tanah di lokasi penanaman. Tambahan pupuk kandang dan kompos akan meningkatkan kandungan organik dalam tanah, sedangkan pasir akan memperbaiki drainase.</p>
                            <h3>Tahukan Anda?</h3>
                            <p style="font-size: 20px;">Durian, si raja buah, memang penuh keunikan. Selain terkenal dengan aroma khasnya yang kuat, durian juga menyimpan segudang manfaat kesehatan. Buah ini kaya akan nutrisi seperti vitamin C, vitamin B kompleks, kalium, dan serat yang baik untuk sistem imun, pencernaan, dan kesehatan jantung. Uniknya, durian memiliki berbagai varietas dengan rasa dan aroma yang berbeda-beda, sehingga selalu ada kejutan baru bagi para penikmatnya. Meski begitu, durian juga mengandung kalori yang cukup tinggi, jadi sebaiknya dikonsumsi secukupnya.</p>
