                            <p style="font-size: 20px;">Untuk merawat tanaman stroberi agar tumbuh subur dan berbuah lebat, perhatikan hal-hal berikut: Pastikan tanaman mendapat sinar matahari cukup, siram secara teratur namun jangan terlalu basah, berikan pupuk organik secara berkala, lakukan pemangkasan rutin untuk merangsang pertumbuhan baru, dan lindungi tanaman dari hama dan penyakit. Jangan lupa untuk memilih lokasi tanam yang memiliki drainase baik agar akar tidak tergenang air. Dengan perawatan yang tepat, Anda bisa menikmati hasil panen stroberi yang segar dan manis.</p>
