                                <li>Biji: Terdapat biji di dalam daging buah.</li>
                            </ul>
                            <h3>Varietas Anggur</h3>
                            <li>Anggur Merah</li>
                            <li>Anggur Hijau</li>
                            <li>Anggur Hitam</li>
                            <h3>Manfaat</h3>
                            <ul style="font-size: 20px;">
                                <li>Menjaga kesehatan jantung</li>
                                <li>Menurunkan tekanan darah</li>
                                <li>Meningkatkan kesehatan pencernaan</li>
                                <li>Melawan radikal bebas</li>
                                <li>Meningkatkan kesehatan otak</li>
                            </ul>
                            <h3>Merawat Tanaman Buah Anggur</h3>
                            <p style="font-size: 20px;">Untuk merawat tanaman anggur agar tumbuh subur dan berbuah lebat, pastikan tanaman mendapatkan sinar matahari yang cukup, siram secara teratur namun jangan terlalu basah, berikan pupuk organik secara berkala, dan lakukan pemangkasan rutin untuk merangsang pertumbuhan baru. Selain itu, lindungi tanaman dari hama dan penyakit. Jangan lupa untuk memberikan ajir atau penopang agar tanaman dapat tumbuh dengan baik. Dengan perawatan yang tepat, Anda dapat menikmati hasil panen anggur yang segar dan manis.</p>
                            <h3>Media Tanam</h3>
                            <p style="font-size: 20px;">Media tanam anggur yang ideal adalah campuran tanah subur, pupuk kandang matang, dan pasir dengan perbandingan 2:1:1. Pastikan pH tanah berkisar antara 6-7. Untuk hasil yang lebih baik, gunakan pupuk organik secara berkala. Hindari penggunaan tanah yang terlalu padat atau terlalu basah karena dapat menghambat pertumbuhan akar.</p>
                            <h3>Tahukan Anda?</h3>
