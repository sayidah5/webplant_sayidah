                            <p style="font-size: 20px;">Anggur adalah buah yang dihasilkan dari tanaman merambat yang termasuk dalam keluarga Vitaceae. Buah ini memiliki beragam warna, mulai dari hijau, kuning, hingga merah tua. Anggur tidak hanya lezat sebagai buah segar, tetapi juga sering diolah menjadi berbagai produk seperti jus, selai, anggur merah, kismis, dan masih banyak lagi.</p>
                            <h3>Karakteristik</h3>
                            <ul style="font-size: 20px;">
                               <li>Bentuk: Bulat atau lonjong, tumbuh dalam tandan.</li>
